# Create a Tuple
mytyple = ("apple", "banana", "cherry")
print(mytyple)

# Allow Duplicates with Index
thistuple = ("apple", "banana", "cherry", "apple", "cherry")
print(thistuple)


# Create a Tuple using a Constructor
newTuple = tuple(("apple", "banana", "cherry")) # note the double round-brackets
print(newTuple)