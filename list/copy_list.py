# make a copy of a list
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist)


# make a copy of a list using constructor of list()
thislist = ["apple", "banana", "cherry"]
mylist = list(thislist)
print(mylist)


# make a copy of a list using slice operator
thislist = ["apple", "banana", "cherry"]
mylist = thislist[1:3]
print(mylist)