thisTuple = ("apple", "banana", "cherry")
# Access Tuple Items
print(thisTuple)

# Access Tuple Item at Index 1
print(thisTuple[1])

# Negative Indexing
print(thisTuple[-1])


# Access with slice operator
print(thisTuple[1:3])

# Check if "apple" is present in the tuple
if "apple" in thisTuple:
    print("Yes, 'apple' is in the fruits tuple")